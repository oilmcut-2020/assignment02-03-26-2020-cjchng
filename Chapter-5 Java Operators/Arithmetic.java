public class Arithmetic{ 
      
    public static void main(String args[]) 
    { 
      
    int a = 10, b = 4, result; 
   
    //printing a and b 
    System.out.println("a is "+a+ " and b is "+ b); 
   
    result = a+b; //addition 
    System.out.println("a+b is "+result); 
   
    result = a-b; //subtraction 
    System.out.println("a-b is "+result); 
   
    result = a*b; //multiplication 
    System.out.println("a*b is "+result); 
   
    result = a/b; //division 
    System.out.println("a/b is "+result); 
   
    result = a%b; //modulus 
    System.out.println("a%b is "+result); 
      
    } 
} 
